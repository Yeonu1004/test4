package ch2023_02_03;

public class LogicAndBinary {

	public static void main(String[] args) {
		
		boolean a = false;
		boolean b = false;
		
		System.out.println(a && b);
		System.out.println(a || b);
		System.out.println(a == b);
		System.out.println(a != b);
		
		int c = 15;
		int d = 8;
		
		System.out.println(c & d);
		System.out.println(c | d);
		System.out.println(~d);
		System.out.println(c ^= d);
		
		

	}
	
}
